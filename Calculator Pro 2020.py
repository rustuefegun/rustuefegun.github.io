num1 = float(input("Say your first number bro: "))
op = input("Say your sign bro: ")
num2 = float(input("Say your second number bro: "))

if op == "+":
    print("Our result is", num1+num2, "buddy")
elif op == "_":
    print("Our result is", num1-num2, "buddy")
elif op == "*":
    print("Our result is", num1*num2, "buddy")
elif op == "/":
    print("Our result is", num1/num2, "buddy")
else: print("i dont know man what is that? INVALID")




